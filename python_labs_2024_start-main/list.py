# Creat a list of fruits
fruits = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]

# Print the list
print(fruits)

# Print the first item in the liat
print(fruits[0])

# Print the last item in the list
print(fruits[-1])

# Change the last item in the list
fruits[-1] = "strawberry"

# loop through the list
for fruit in fruits:
    print(fruit)





